package com.hussainIT;

public class AddSubMulDev {

	public static void main(String[] args) {
		double x=1.000;
		double y=5.000;
		double addValue=x+y;
		double subVal=x-y;
		double mult=x* y;
		double div= x/y ; 
		System.out.printf("x + y = %.4f\n",addValue);
		System.out.printf("x-y =%.4f\n ",subVal);
		System.out.printf("x * y = %.4f \n",mult );
		System.out.printf(" x / y = %.4f\n",div);
	}

}
